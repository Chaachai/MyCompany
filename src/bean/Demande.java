package bean;

import java.util.Date;

/**
 *
 * @author CHAACHAI Youssef
 */
public class Demande {

    private Long id;
    private String message;
    private Date date;
    private int etat;
    private Salarie salarie;
    private TypeDemande typeDemande;

    public Demande() {
    }

    public Demande(Long id) {
        this.id = id;
    }

    public Demande(Long id, String message, Date date, int etat, Long id_salarie, int id_typeDemande) {
        this.id = id;
        this.message = message;
        this.date = date;
        this.etat = etat;
        salarie = new Salarie(id_salarie);
        typeDemande = new TypeDemande(id_typeDemande);
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getEtat() {
        return etat;
    }

    public void setEtat(int etat) {
        this.etat = etat;
    }

    public Salarie getSalarie() {
        return salarie;
    }

    public void setSalarie(Salarie salarie) {
        this.salarie = salarie;
    }

    public TypeDemande getTypeDemande() {
        return typeDemande;
    }

    public void setTypeDemande(TypeDemande typeDemande) {
        this.typeDemande = typeDemande;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Demande)) {
            return false;
        }
        Demande other = (Demande) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "bean.Demande[ id=" + id + " ]";
    }

}
